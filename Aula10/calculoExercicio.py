def soma(x,y):
    adicao = x + y
    return adicao

def sub(x,y):
    menos = x - y
    return menos 

def multi(x,y):
    vezes = x * y
    return vezes

def div(x,y):
    divisao = x // y
    return divisao

def div_fra(x,y):
    divisao_fra = x / y
    return divisao_fra

def rest_div(x,y):
    restante = x % y
    return restante

def raiz(x,y):
    raizx = x**(1/y)
    return raizx

